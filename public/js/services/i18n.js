export const I18n = {
    currentLang: localStorage.getItem('app_language'),

    init() {
        if (!this.currentLang) {
            const sysLang = navigator.language || navigator.userLanguage;
            const langCode = sysLang.split('-')[0].toLowerCase();

            if (sysLang.toLowerCase().includes('zh')) this.currentLang = 'zh-TW';
            else this.currentLang = 'en';

            console.log(`[I18n] Auto-detected language: ${sysLang} -> ${this.currentLang}`);
            localStorage.setItem('app_language', this.currentLang);
        }
    },

    getLanguage() {
        if (!this.currentLang) this.init();
        return this.currentLang;
    },

    setLanguage(lang) {
        if (Object.keys(Translations).includes(lang)) {
            // Save previous language for comparison if needed
            const prevLang = this.currentLang;
            this.currentLang = lang;
            localStorage.setItem('app_language', lang);
            window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang, prevLang } }));
            return true;
        }
        return false;
    },

    t(key, params = {}) {
        if (!this.currentLang) this.init();
        const dict = Translations[this.currentLang] || Translations['en'];
        let text = dict[key] || key;

        // Simple interpolation
        Object.keys(params).forEach(p => {
            text = text.replace(`{${p}}`, params[p]);
        });

        return text;
    }
};

I18n.init();
window.I18n = I18n;
